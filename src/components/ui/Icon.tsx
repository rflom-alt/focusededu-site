export function Icon({
  name,
  className,
  strokeWidth = 1.6,
}: {
  name: string;
  className?: string;
  strokeWidth?: number;
}) {
  const Cmp = registry[name] ?? ShieldCheck;
  return <Cmp className={className} strokeWidth={strokeWidth} aria-hidden />;
}
